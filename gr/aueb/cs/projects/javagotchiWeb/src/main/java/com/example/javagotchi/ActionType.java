package com.example.javagotchi;

/**
 * Καθορίζει τους διαθέσιμους τύπους ενεργειών (actions) που μπορεί να
 * εκτελέσει ένας χρήστης στο καταφύγιό του.
 * Η χρήση Enum αντί για απλά αλφαριθμητικά (Strings) διασφαλίζει την ασφάλεια τύπων (type safety)
 * και αποτρέπει σφάλματα από τυπογραφικά λάθη (π.χ. "adopt" αντί για "Adopt") κατά τον έλεγχο των αιτημάτων.
 */
public enum ActionType {

    /** Αντιπροσωπεύει την ενέργεια υιοθεσίας ενός νέου κατοικιδίου σε ένα άδειο κλουβί. */
    ADOPT,

    /** Αντιπροσωπεύει την ενέργεια ταΐσματος για την αύξηση του κορεσμού ενός κατοικιδίου. */
    FEED,

    /** Αντιπροσωπεύει την ενέργεια παιχνιδιού για την αύξηση της ευτυχίας ενός κατοικιδίου. */
    PLAY,

    /** Αντιπροσωπεύει την ενέργεια καθαρισμού ενός κλουβιού (επιτρέπεται μόνο όταν το κατοικίδιο έχει πεθάνει). */
    CLEAN
}
